TempCelsius = float(input("Enter the Temperature in Celsius:"))
print("Temperature in Celsius :",TempCelsius,"°C")

#Equivalent in Fahrenheit 
TempFahrenheit = 9/5*TempCelsius+32
print("Equivalent Temperature in Fahrenheit :",TempFahrenheit,"°F")

#Equivalent in Kelvin
TempKelvin = TempCelsius + 273.15
print("Equivalent Temperature in Kelvin :",TempKelvin,"K")

